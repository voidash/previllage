#!/usr/bin/env python3
"""Generate grounded eval items via MURI-style reverse-instruction.

For each substantive chunk in the gov-corpus dump, ask Sonnet "what realistic
citizen question would this chunk answer?". Output is a draft eval item where
the gold source is known by construction.

This complements `build_groundedness_eval.py`: that one runs reddit questions
through retrieval (mostly produces refusal items because the corpus is sparse
on citizen-facing pages); this one starts from chunks we KNOW we have, so by
construction every output has a gold source.

Schema per output line:
    {
        "id":                 "eval_g_001",
        "source":             "reverse_instruction",
        "question":           "<generated by Sonnet>",
        "question_lang":      "devanagari" | "roman_nepali" | "english" | "code_mixed",
        "question_category":  "passport" | "citizenship" | ... | "other",
        "difficulty":         "easy" | "medium" | "hard",
        "answer_summary":     "<one-sentence answer summary from Sonnet>",
        "gold_chunk":         {"chunk_id":..., "url":..., "text":..., "source_id":..., "tier":...},
        "review":             {"verdict": null, ...}
    }

Usage:
    python scripts/generate_grounded_eval.py --limit 5    # smoke test (1 batch of 5)
    python scripts/generate_grounded_eval.py --n 100      # full run
"""
from __future__ import annotations

import argparse
import json
import logging
import os
import random
import sys
import time
import urllib.error
import urllib.request
from concurrent.futures import ThreadPoolExecutor, as_completed
from pathlib import Path
from threading import Lock
from typing import Optional

MERIDIAN_URL = os.environ.get("MERIDIAN_URL", "http://127.0.0.1:3456")
MODEL = os.environ.get("GENERATOR_MODEL", "claude-sonnet-4-6")

# Citizen-facing source allocation. We over-weight Jiri (demo target) and
# the federal procedural ministries. Numbers should sum to ~100.
DEFAULT_ALLOCATION = {
    "jirimun_gov_np":  30,  # Jiri Municipality (demo target)
    "moha_gov_np":     15,  # passport, citizenship, NID
    "nta_gov_np":      10,  # telecom/SIM/phone registration
    "nrb_org_np":      10,  # banking rules, FX, foreign investment
    "doanepal_gov_np":  8,  # agriculture
    "doind_gov_np":     8,  # industry registration
    "edcd_gov_np":      7,  # epidemiology / health
    "ciaa_gov_np":      5,  # anti-corruption (citizen complaints)
    "dls_gov_np":       4,  # livestock (rural citizens)
    "sebon_gov_np":     3,  # securities (investor procedures)
}

# Chunk text length filter — too short = navigation/header, too long = multi-topic.
CHUNK_MIN_CHARS = 600
CHUNK_MAX_CHARS = 3000
CHUNK_TEXT_FOR_PROMPT_MAX = 1500

VALID_CATEGORIES = {
    "passport", "citizenship", "tax", "land", "business", "education",
    "driving_license", "pan_vat", "birth_registration", "marriage",
    "visa_immigration", "police", "agriculture", "health", "telecom",
    "banking", "investment", "industry", "local_services", "other",
}
VALID_LANGS = {"devanagari", "roman_nepali", "code_mixed"}
VALID_DIFFICULTIES = {"easy", "medium", "hard"}

MAX_RETRIES = 3
TIMEOUT_S = 90

PROMPT = """\
You are creating eval items for a Nepal-government helpdesk groundedness benchmark.

For each provided gov.np page excerpt, generate ONE realistic question a Nepal
citizen would ask which the excerpt directly answers. The question must be
genuinely answerable from THIS excerpt alone (no outside knowledge).

Rules for question generation:
- Write as a real Nepal citizen would phrase it on a helpdesk — natural,
  sometimes informal.
- **Question language MUST be one of: "devanagari", "roman_nepali", or
  "code_mixed". Never "english".** Even if the source excerpt is in English
  (regulatory PDF, English annual report, etc.), the citizen would still ask
  in Nepali. Pick:
    * "devanagari" for formal / policy / ministry topics where users tend to
      type Nepali (citizenship, taxes, official procedures)
    * "roman_nepali" for everyday / local / informal topics (Jiri palika,
      driving license, online registration)
    * "code_mixed" when English technical terms naturally appear inside an
      otherwise Nepali question (PAN, VAT, digital wallet, etc.)
- Avoid trivial fact-recall ("what is the title of this document"). Prefer
  procedural / informational queries ("how do I X", "what's the rule about Y",
  "where do I go for Z", "is X allowed").
- Avoid analyst / specialist questions (annual inflation rates, foreign
  investment statistics) — focus on what an actual citizen would ask.
- If the excerpt is too generic / boilerplate / repetitive / English-only-
  technical / not citizen-relevant to support a meaningful Nepali question,
  set "skip": true.

Return STRICT JSON array of EXACTLY {n} items, in input order. No markdown
fences. Each item:
{{
  "chunk_id": "<echo the input chunk_id>",
  "skip": <bool — true if no good question can be generated>,
  "question": "<question text, blank if skip>",
  "language": "devanagari" | "roman_nepali" | "english" | "code_mixed",
  "category": one of: passport | citizenship | tax | land | business | education | driving_license | pan_vat | birth_registration | marriage | visa_immigration | police | agriculture | health | telecom | banking | investment | industry | local_services | other,
  "difficulty": "easy" | "medium" | "hard",
  "answer_summary": "<one-sentence summary of how the excerpt answers the question>"
}}

Excerpts ({n} total):

{items}

JSON array (length {n}):"""


def call_meridian(prompt: str, max_tokens: int) -> str:
    """POST to Meridian /v1/messages. Raises on persistent failure."""
    payload = json.dumps(
        {
            "model": MODEL,
            "max_tokens": max_tokens,
            "messages": [{"role": "user", "content": prompt}],
        }
    ).encode("utf-8")
    last_err: Optional[Exception] = None
    for attempt in range(MAX_RETRIES):
        try:
            req = urllib.request.Request(
                f"{MERIDIAN_URL}/v1/messages",
                data=payload,
                headers={
                    "Content-Type": "application/json",
                    "x-api-key": "x",
                    "anthropic-version": "2023-06-01",
                },
                method="POST",
            )
            with urllib.request.urlopen(req, timeout=TIMEOUT_S) as resp:
                data = json.loads(resp.read())
            if "content" not in data:
                raise RuntimeError(f"missing 'content' in response: {data}")
            parts = [b.get("text", "") for b in data["content"] if b.get("type") == "text"]
            return "".join(parts).strip()
        except urllib.error.HTTPError as e:
            err_body = e.read().decode("utf-8", errors="replace")
            last_err = RuntimeError(f"HTTP {e.code}: {err_body[:300]}")
            if e.code in (429, 500, 502, 503, 504):
                time.sleep(2 ** attempt)
                continue
            raise last_err
        except (urllib.error.URLError, TimeoutError, OSError, json.JSONDecodeError) as e:
            last_err = e
            time.sleep(2 ** attempt)
    raise RuntimeError(f"Meridian call failed after {MAX_RETRIES} attempts: {last_err}")


def parse_response(raw: str, expected_n: int) -> list[dict]:
    """Parse a JSON array. Strips code fences if present. Raises on malformed."""
    s = raw.strip()
    if s.startswith("```"):
        s = s.split("\n", 1)[1] if "\n" in s else s
        if s.endswith("```"):
            s = s.rsplit("```", 1)[0]
        s = s.strip()
    parsed = json.loads(s)
    if not isinstance(parsed, list):
        raise ValueError(f"expected JSON array, got {type(parsed).__name__}")
    if len(parsed) != expected_n:
        raise ValueError(f"expected {expected_n} items, got {len(parsed)}")
    return parsed


def select_chunks(corpus_path: Path, allocation: dict[str, int], rng: random.Random) -> list[dict]:
    """Stream-load corpus, filter to substantive chunks, sample per source."""
    by_source: dict[str, list[dict]] = {sid: [] for sid in allocation}
    skipped_short = 0
    skipped_long = 0
    skipped_other_source = 0
    seen_total = 0

    with corpus_path.open(encoding="utf-8") as f:
        for line in f:
            seen_total += 1
            r = json.loads(line)
            sid = r.get("source_id")
            if sid not in by_source:
                skipped_other_source += 1
                continue
            text = r.get("text") or ""
            if len(text) < CHUNK_MIN_CHARS:
                skipped_short += 1
                continue
            if len(text) > CHUNK_MAX_CHARS:
                skipped_long += 1
                continue
            by_source[sid].append(r)

    logging.info(
        "corpus scan: %d total chunks; %d skipped (other src), %d short, %d long",
        seen_total,
        skipped_other_source,
        skipped_short,
        skipped_long,
    )
    selected: list[dict] = []
    for sid, target_n in allocation.items():
        pool = by_source[sid]
        if not pool:
            logging.warning("no eligible chunks for %s", sid)
            continue
        rng.shuffle(pool)
        take = min(target_n, len(pool))
        if take < target_n:
            logging.warning("%s: requested %d but only %d eligible", sid, target_n, take)
        selected.extend(pool[:take])
        logging.info("  %s: selected %d / pool %d", sid, take, len(pool))
    rng.shuffle(selected)  # mix source order so concurrent batches see variety
    return selected


def generate_batch(chunks: list[dict]) -> list[dict]:
    """Send N chunks in one call; returns list of result records (in input order).

    On batch-level failure, returns a list of error placeholders for each chunk
    so we don't block the rest of the run.
    """
    if not chunks:
        return []
    items_text = "\n\n".join(
        f"[{i+1}] chunk_id={c['chunk_id']} url={c['source_url']}\n{c['text'][:CHUNK_TEXT_FOR_PROMPT_MAX]}"
        for i, c in enumerate(chunks)
    )
    prompt = PROMPT.format(n=len(chunks), items=items_text)
    max_out = max(800, 320 * len(chunks))
    try:
        raw = call_meridian(prompt, max_tokens=max_out)
        parsed = parse_response(raw, len(chunks))
    except Exception as e:
        logging.warning("batch of %d failed (%s: %s)", len(chunks), type(e).__name__, str(e)[:160])
        return [_error_placeholder(c, e) for c in chunks]

    out: list[dict] = []
    for c, p in zip(chunks, parsed):
        if not isinstance(p, dict):
            out.append(_error_placeholder(c, ValueError(f"non-dict item: {p}")))
            continue
        out.append(_assemble_record(c, p))
    return out


def _error_placeholder(chunk: dict, err: Exception) -> dict:
    return {
        "source": "reverse_instruction",
        "question": None,
        "question_lang": None,
        "question_category": None,
        "difficulty": None,
        "answer_summary": None,
        "skip": True,
        "skip_reason": f"{type(err).__name__}: {str(err)[:200]}",
        "gold_chunk": _gold_chunk(chunk),
    }


def _gold_chunk(c: dict) -> dict:
    return {
        "chunk_id": c.get("chunk_id"),
        "url": c.get("source_url"),
        "source_id": c.get("source_id"),
        "tier": c.get("tier"),
        "text": c.get("text"),
    }


def _assemble_record(chunk: dict, p: dict) -> dict:
    skip = bool(p.get("skip"))
    question = (p.get("question") or "").strip()
    lang = p.get("language")
    cat = p.get("category")
    diff = p.get("difficulty")
    if not skip and not question:
        skip = True  # treat empty question as a skip
    if skip:
        return {
            "source": "reverse_instruction",
            "question": None,
            "question_lang": None,
            "question_category": None,
            "difficulty": None,
            "answer_summary": (p.get("answer_summary") or None),
            "skip": True,
            "skip_reason": "model marked skip",
            "gold_chunk": _gold_chunk(chunk),
        }
    if lang not in VALID_LANGS:
        lang = "english"
    if cat not in VALID_CATEGORIES:
        cat = "other"
    if diff not in VALID_DIFFICULTIES:
        diff = "medium"
    return {
        "source": "reverse_instruction",
        "question": question,
        "question_lang": lang,
        "question_category": cat,
        "difficulty": diff,
        "answer_summary": (p.get("answer_summary") or "").strip()[:500] or None,
        "skip": False,
        "gold_chunk": _gold_chunk(chunk),
    }


def already_done_chunk_ids(out_path: Path) -> set[str]:
    if not out_path.exists():
        return set()
    seen: set[str] = set()
    with out_path.open(encoding="utf-8") as f:
        for line in f:
            try:
                cid = json.loads(line).get("gold_chunk", {}).get("chunk_id")
                if cid:
                    seen.add(cid)
            except (json.JSONDecodeError, KeyError):
                continue
    return seen


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--corpus", default="corpora/gov_chunks_v2.jsonl")
    ap.add_argument("--output", default="eval/gov_helpdesk_v1_grounded.jsonl")
    ap.add_argument("--n", type=int, default=100)
    ap.add_argument("--limit", type=int, default=0, help="0 = process all selected")
    ap.add_argument("--batch-size", type=int, default=5)
    ap.add_argument("--concurrency", type=int, default=3)
    ap.add_argument("--seed", type=int, default=42)
    ap.add_argument("--verbose", action="store_true")
    args = ap.parse_args()

    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(asctime)s %(levelname)s %(message)s",
    )

    corpus_path = Path(args.corpus)
    out_path = Path(args.output)
    out_path.parent.mkdir(parents=True, exist_ok=True)

    if not corpus_path.exists():
        print(f"corpus not found: {corpus_path}", file=sys.stderr)
        return 1

    # Scale allocation to args.n while preserving relative weights.
    base_total = sum(DEFAULT_ALLOCATION.values())
    allocation = {
        sid: max(1, round(target * args.n / base_total))
        for sid, target in DEFAULT_ALLOCATION.items()
    }
    logging.info("target allocation (n=%d): %s", args.n, allocation)

    rng = random.Random(args.seed)
    chunks = select_chunks(corpus_path, allocation, rng)
    logging.info("selected %d chunks total", len(chunks))

    done_ids = already_done_chunk_ids(out_path)
    if done_ids:
        before = len(chunks)
        chunks = [c for c in chunks if c["chunk_id"] not in done_ids]
        logging.info("skipping %d already-done chunks", before - len(chunks))

    if args.limit > 0:
        chunks = chunks[: args.limit]
        logging.info("limit=%d; processing %d", args.limit, len(chunks))

    if not chunks:
        logging.info("nothing to do")
        return 0

    batches = [chunks[i : i + args.batch_size] for i in range(0, len(chunks), args.batch_size)]
    logging.info(
        "submitting %d batches of up to %d, concurrency=%d",
        len(batches),
        args.batch_size,
        args.concurrency,
    )

    write_lock = Lock()
    n_done = 0
    n_kept = 0
    n_skipped = 0
    t0 = time.time()
    with out_path.open("a", encoding="utf-8") as f_out, ThreadPoolExecutor(
        max_workers=args.concurrency
    ) as pool:
        futs = {pool.submit(generate_batch, b): b for b in batches}
        for fut in as_completed(futs):
            results = fut.result()
            with write_lock:
                for row in results:
                    f_out.write(json.dumps(row, ensure_ascii=False) + "\n")
                    n_done += 1
                    if row.get("skip"):
                        n_skipped += 1
                    else:
                        n_kept += 1
                f_out.flush()
                if n_done % args.batch_size == 0 or n_done >= len(chunks):
                    elapsed = time.time() - t0
                    rate = n_done / elapsed if elapsed > 0 else 0
                    eta = (len(chunks) - n_done) / rate if rate > 0 else 0
                    logging.info(
                        "%d/%d (%.2f rec/s, eta %.0fs) | kept=%d skipped=%d",
                        n_done,
                        len(chunks),
                        rate,
                        eta,
                        n_kept,
                        n_skipped,
                    )

    print(f"\n=== generator summary ===", file=sys.stderr)
    print(f"  processed: {n_done}", file=sys.stderr)
    print(f"  kept:      {n_kept}", file=sys.stderr)
    print(f"  skipped:   {n_skipped}", file=sys.stderr)
    print(f"  output:    {out_path}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
